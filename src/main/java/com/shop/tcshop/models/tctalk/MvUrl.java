package com.shop.tcshop.models.tctalk;

public class MvUrl {

    private int id;
    private String url;
    private int r;
    private int size;
    private String md5;
    private String code;
    private String expi;
    private int fee;
    private int mvFee;
    private int st;
    private String promotionVo;
    private String msg;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getExpi() {
        return expi;
    }

    public void setExpi(String expi) {
        this.expi = expi;
    }

    public int getFee() {
        return fee;
    }

    public void setFee(int fee) {
        this.fee = fee;
    }

    public int getMvFee() {
        return mvFee;
    }

    public void setMvFee(int mvFee) {
        this.mvFee = mvFee;
    }

    public int getSt() {
        return st;
    }

    public void setSt(int st) {
        this.st = st;
    }

    public String getPromotionVo() {
        return promotionVo;
    }

    public void setPromotionVo(String promotionVo) {
        this.promotionVo = promotionVo;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
