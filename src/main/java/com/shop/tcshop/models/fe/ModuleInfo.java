package com.shop.tcshop.models.fe;

/**
 * 直播信息
 */
public class ModuleInfo {
    /**
     * 标题
     */
    private String title;

    /**
     * 链接地址
     */
    private String link;


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}
